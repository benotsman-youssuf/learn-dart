/*Challenge 4: Average Rating
Declare three constants called rating1 , rating2 and rating3 of type double and assign each
a value. Calculate the average of the three and store the result in a constant named
averageRating .*/

void main() {
  const double rating1 = 20;
  const double rating2 = 93;
  const double rating3 = 923;

  const averageRating = (rating3 + rating2 + rating1) / 3;
  print(averageRating);
}
