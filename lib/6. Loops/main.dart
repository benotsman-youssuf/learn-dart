void main() {
  //!------------while loop------------
  int i = 1;
  while (i <= 5) {
    print("Value of i: $i");
    i++;
  }
  print('\n');
  //!------------do while loop------------
  int j = 1;
  do {
    print("Value of j: $j");
    j++;
  } while (j <= 5);
  print('\n');

  //!------------for loop------------
  for (int j = 1; j <= 5; j++) {
    print("Value of m: $j");
  }
}
